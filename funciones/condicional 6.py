def año(dias):
    if dias < 31:
        print ("enero")
    elif dias < 59:
        print ("febrero")
    elif dias < 90:
        print ("marzo")
    elif dias < 120:
        print ("abril")
    elif dias < 151:
        print ("mayo")
    elif dias < 181:
        print ("junio")
    elif dias < 212:
        print ("julio")
    elif dias < 243:
        print ("agosto")
    elif dias < 273:
        print ("septiembre")
    elif dias < 304:
        print ("octubre")
    elif dias < 334:
        print ("noviembre")
    elif dias < 365:
        print ("diciembre")


dias = int(input("digite cuantos dias"))
año(dias)