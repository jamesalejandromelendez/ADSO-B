def suma (a,b):#se crea un funcion llamada 'suma' con dos parametros 'a' y 'b'
    return a+b#retorna el valor de la suma de los dos parametros dados 

def suma(a,b,c):#se crea un funcion llamada 'suma' con tres parametros 'a', 'b' y 'c'
    return a+b+c#retorna el valor de la suma de los dos parametros dados 

print(suma(1,2,3))#llama la funcion dentro de un print y agrega tres argumentos 
                  #al tener los tre parametro va hacer el llamado de la segunda o ultima funcion
                  #ya que con esta cumple con los parametros solicitados