    if x==y==z:
        return "todos son iguales"
    elif x == y:
        return "el primero y segundo numeros son iguales"
    elif y == z:
        return "el segundo y tercer numeros son iguales"
    elif y == z:
        return "el segundo y tercer numeros son iguales"
    else :
        return "no hay numeros iguales"
